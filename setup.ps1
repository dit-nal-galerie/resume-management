# Инициализация проекта
npm init -y

# Установка пакетов для backend
npm install express mysql2 body-parser bcrypt

# Установка пакетов типов для TypeScript (если вы работаете с TypeScript)
npm install --save-dev @types/express @types/node @types/bcrypt

# Установка пакетов для frontend (React)
npm install react react-dom react-router-dom

# Установка дополнительных типов для React и роутера (если используете TypeScript)
npm install --save-dev @types/react @types/react-dom @types/react-router-dom

# Установка дополнительных UI библиотек (если выбрали React-Bootstrap)
npm install react-bootstrap bootstrap

npm install dotenv

